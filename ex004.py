# Specify something
x = input('Type something: ')


print('What is the type? {}' .format(type(x)))
print('Is it letter or number?', x.isalnum())
print('Is it letter? {}' .format(x.isalpha()))
print('Is it number? {}' .format(x.isnumeric()))
print('Is all capital letter? {}' .format(x.isupper()))
print('There is space? {}' .format(x.isspace()))
print('Is it all lowercase? {}' .format(x.islower()))








